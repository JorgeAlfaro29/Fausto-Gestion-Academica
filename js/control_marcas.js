// Mostrar todas las marcas
function renderizarMarcas(marcas) {
  const tabla = document.getElementById("tablaControlMarcas");
  tabla.innerHTML = '';

  if (marcas.length === 0) {
    tabla.innerHTML = '<tr><td colspan="6" class="text-center text-muted">No hay marcas registradas.</td></tr>';
    return;
  }

  marcas.forEach(m => {
    const fila = document.createElement('tr');
    fila.innerHTML = `
      <td>${m.nombre}</td>
      <td>${m.cedula}</td>
      <td>${m.fecha}</td>
      <td>${m.entrada}</td>
      <td>${m.salida}</td>
      <td>${m.horas} h</td>
    `;
    tabla.appendChild(fila);
  });
}

// Filtro dinámico
document.getElementById('filtroCedula').addEventListener('input', aplicarFiltros);
document.getElementById('filtroFecha').addEventListener('change', aplicarFiltros);

function aplicarFiltros() {
  const cedula = document.getElementById('filtroCedula').value.trim();
  const fecha = document.getElementById('filtroFecha').value;

  const filtradas = marcasRegistradas.filter(m => {
    const matchCedula = !cedula || m.cedula.includes(cedula);
    const matchFecha = !fecha || m.fecha === fecha;
    return matchCedula && matchFecha;
  });

  renderizarMarcas(filtradas);
}

// Inicialización
document.addEventListener('DOMContentLoaded', () => {
  renderizarMarcas(marcasRegistradas);
});
